// Largest Element

import java.util.Scanner;

public class Question10 {
    
    public static int largest(int[] arr) {
        int max = arr[0];
        for(int i = 1; i < arr.length; i++){
            if(max < arr[i]){
                max = arr[i];
            }
        }
        return max;
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr = new int[n];
        for(int i = 0; i < n; i++){
            arr[i] = sc.nextInt();
        }
        int ans = largest(arr);
        System.out.println(ans);
        sc.close();
    }
}
